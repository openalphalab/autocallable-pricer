constexpr int MAX_SOBOL_DIM = 200;
constexpr int SOBOL_BITS = 30;

class SobolGenerator {
public:
    SobolGenerator(int dim) : dim_(dim) {
        if (dim < 1 || dim > MAX_SOBOL_DIM) {
            throw std::runtime_error("sobol dim out of range (1.." + std::to_string(MAX_SOBOL_DIM) + ")");
        }
        V_.assign(dim_, std::array<uint32_t, SOBOL_BITS>{});
        for (int i = 0; i < SOBOL_BITS; ++i) V_[0][i] = 1u << (SOBOL_BITS - 1 - i);
        for (int d = 1; d < dim_; ++d) {
            const JKEntry& e = JK[d - 1];
            int s = e.s; int a = e.a;
            for (int i = 0; i < s; ++i) V_[d][i] = e.m[i] << (SOBOL_BITS - 1 - i);
            for (int i = s; i < SOBOL_BITS; ++i) {
                uint32_t v = V_[d][i - s] ^ (V_[d][i - s] >> s);
                for (int k = 1; k < s; ++k) {
                    if ((a >> (s - 1 - k)) & 1) v ^= V_[d][i - k];
                }
                V_[d][i] = v;
            }
        }
        reset();
    }
    void reset() { X_.assign(dim_, 0); count_ = 0; }
    void next(double* out) {
        ++count_;
        uint32_t c = count_;
        int j = 0;
        while (c & 1) { c >>= 1; ++j; }
        for (int d = 0; d < dim_; ++d) {
            X_[d] ^= V_[d][j];
            out[d] = (static_cast<double>(X_[d]) + 0.5) / (double)(1u << SOBOL_BITS);
        }
    }
    int dim() const { return dim_; }
private:
    int dim_;
    uint64_t count_ = 0;
    std::vector<std::array<uint32_t, SOBOL_BITS>> V_;
    std::vector<uint32_t> X_;
};

class ScrambledSobol {
public:
    ScrambledSobol(int dim, uint64_t seed) : sobol_(dim), masks_(dim) {
        uint64_t s = seed ? seed : 0x9E3779B97F4A7C15ULL;
        for (int d = 0; d < dim; ++d) {
            s += 0x9E3779B97F4A7C15ULL;
            uint64_t z = s;
            z = (z ^ (z >> 30)) * 0xBF58476D1CE4E5B9ULL;
            z = (z ^ (z >> 27)) * 0x94D049BB133111EBULL;
            z = z ^ (z >> 31);
            masks_[d] = (uint32_t)(z >> 2) & ((1u << SOBOL_BITS) - 1);
        }
    }
    void reset() { sobol_.reset(); }
    void next(double* out) {
        sobol_.next(out);
        for (int d = 0; d < sobol_.dim(); ++d) {
            uint32_t xi = (uint32_t)(out[d] * (double)(1u << SOBOL_BITS)) ^ masks_[d];
            out[d] = (static_cast<double>(xi) + 0.5) / (double)(1u << SOBOL_BITS);
        }
    }
    int dim() const { return sobol_.dim(); }
private:
    SobolGenerator sobol_;
    std::vector<uint32_t> masks_;
};

